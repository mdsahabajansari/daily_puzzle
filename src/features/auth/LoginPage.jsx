import { useState } from 'react';
import { motion } from 'framer-motion';
import { useNavigate } from 'react-router-dom';
import { signInWithGoogle, signInWithTruecaller, signOut } from '@/services/authService';
import { useAuthStore } from './AuthProvider';

export default function LoginPage() {
    const authState = useAuthStore();
    const user = authState?.user || null;
    const loading = authState?.loading ?? true;
    const navigate = useNavigate();
    const [error, setError] = useState(null);
    const [signingIn, setSigningIn] = useState(false);

    const handleGoogleLogin = async () => {
        setError(null);
        setSigningIn(true);
        try {
            await signInWithGoogle();
            navigate('/');
        } catch (err) {
            console.error('Google sign-in failed:', err);
            // Provide user-friendly error messages
            if (err.code === 'auth/popup-closed-by-user') {
                setError('Sign-in popup was closed. Please try again.');
            } else if (err.code === 'auth/network-request-failed') {
                setError('Network error. Check your internet connection.');
            } else if (err.code === 'auth/api-key-not-valid.-please-pass-a-valid-api-key.' || err.code === 'auth/invalid-api-key') {
                setError('Firebase is not configured. Please set up your .env file with valid Firebase credentials.');
            } else if (err.code === 'auth/unauthorized-domain') {
                setError('This domain is not authorized in Firebase. Add localhost to your Firebase Console > Authentication > Settings > Authorized domains.');
            } else {
                setError(err.message || 'Sign-in failed. Please try again.');
            }
        } finally {
            setSigningIn(false);
        }
    };

    const handleLogout = async () => {
        try {
            await signOut();
        } catch (err) {
            console.error('Sign out failed:', err);
        }
    };

    if (loading) {
        return (
            <div className="flex flex-col items-center justify-center min-h-[60vh] gap-4">
                <motion.div
                    animate={{ rotate: 360 }}
                    transition={{ duration: 4, repeat: Infinity, ease: 'linear' }}
                    className="text-6xl"
                >
                    üß©
                </motion.div>
                <div className="text-slate-500 font-mono text-[10px] uppercase tracking-widest animate-pulse">
                    Authenticating...
                </div>
            </div>
        );
    }

    if (user) {
        return (
            <motion.div
                initial={{ opacity: 0, scale: 0.95 }}
                animate={{ opacity: 1, scale: 1 }}
                className="max-w-md mx-auto mt-12"
            >
                <div className="glass p-10 text-center space-y-6">
                    <div className="relative inline-block">
                        <motion.div
                            animate={{ scale: [1, 1.05, 1], rotate: [0, 5, -5, 0] }}
                            transition={{ duration: 6, repeat: Infinity }}
                            className="absolute -inset-4 bg-brand-500/20 rounded-full blur-xl"
                        />
                        {user.photoURL ? (
                            <img
                                src={user.photoURL}
                                alt={user.displayName || 'User'}
                                className="relative w-24 h-24 rounded-3xl mx-auto border-2 border-brand-500/50 object-cover shadow-2xl"
                            />
                        ) : (
                            <div className="relative w-24 h-24 rounded-3xl mx-auto bg-brand-600 flex items-center justify-center text-4xl text-white shadow-2xl">
                                {user.displayName?.charAt(0) || 'P'}
                            </div>
                        )}
                    </div>

                    <div>
                        <h2 className="text-2xl font-black tracking-tight text-white">{user.displayName || 'Elite Player'}</h2>
                        <p className="text-slate-500 font-mono text-[10px] uppercase tracking-widest mt-1">{user.email}</p>
                    </div>

                    <div className="pt-4 space-y-4">
                        <button
                            onClick={() => navigate('/')}
                            className="btn-primary w-full flex items-center justify-center gap-3 text-sm font-black uppercase tracking-widest"
                        >
                            <span>BACK TO GAME</span>
                            <span className="text-xl">üéÆ</span>
                        </button>
                        <button
                            onClick={handleLogout}
                            className="w-full py-4 text-[10px] font-black uppercase tracking-[0.3em] text-slate-500 hover:text-red-400 transition-colors"
                        >
                            Sign Out
                        </button>
                    </div>
                </div>
            </motion.div>
        );
    }

    return (
        <motion.div
            initial={{ opacity: 0, y: 30 }}
            animate={{ opacity: 1, y: 0 }}
            className="max-w-md mx-auto mt-12"
        >
            <div className="glass p-10 space-y-10">
                <div className="text-center space-y-3">
                    <motion.div
                        animate={{ y: [0, -10, 0] }}
                        transition={{ duration: 4, repeat: Infinity, ease: 'easeInOut' }}
                        className="text-6xl mb-4"
                    >
                        üß©
                    </motion.div>
                    <h2 className="text-3xl font-black tracking-tighter text-gradient">IDENTIFY YOURSELF</h2>
                    <p className="text-slate-400 text-sm leading-relaxed px-4">
                        Secure your progress and sync achievements across your digital neural network.
                    </p>
                </div>

                {/* Error Message */}
                {error && (
                    <motion.div
                        initial={{ opacity: 0, y: -10 }}
                        animate={{ opacity: 1, y: 0 }}
                        className="p-4 rounded-xl bg-red-500/10 border border-red-500/30 text-red-300 text-sm text-center"
                    >
                        <p className="text-xs font-bold uppercase tracking-wide mb-1">‚ö†Ô∏è Authentication Error</p>
                        <p className="text-[11px] leading-relaxed">{error}</p>
                    </motion.div>
                )}

                <div className="space-y-4">
                    <motion.button
                        whileHover={{ scale: 1.02, y: -2 }}
                        whileTap={{ scale: 0.98 }}
                        onClick={handleGoogleLogin}
                        disabled={signingIn}
                        className={`w-full py-4 px-6 rounded-2xl bg-white text-gray-950 font-black text-xs uppercase tracking-widest flex items-center justify-center gap-4 hover:shadow-xl transition-all ${signingIn ? 'opacity-60 cursor-wait' : ''}`}
                    >
                        {signingIn ? (
                            <>
                                <motion.div
                                    animate={{ rotate: 360 }}
                                    transition={{ duration: 1, repeat: Infinity, ease: 'linear' }}
                                    className="w-5 h-5 border-2 border-gray-400 border-t-gray-900 rounded-full"
                                />
                                Connecting...
                            </>
                        ) : (
                            <>
                                <svg viewBox="0 0 24 24" width="20" height="20">
                                    <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92a5.06 5.06 0 0 1-2.2 3.32v2.76h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" />
                                    <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" />
                                    <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" />
                                    <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" />
                                </svg>
                                Neural Link (Google)
                            </>
                        )}
                    </motion.button>

                    <motion.button
                        whileHover={{ scale: 1.02, y: -2 }}
                        whileTap={{ scale: 0.98 }}
                        onClick={() => alert('Truecaller sync requires mobile environment.')}
                        className="w-full py-4 px-6 rounded-2xl bg-[#0099FF] text-white font-black text-xs uppercase tracking-widest flex items-center justify-center gap-4 hover:shadow-xl shadow-lg shadow-[#0099FF]/20 transition-all opacity-80"
                    >
                        üì± Biometric Sync
                    </motion.button>
                </div>

                <div className="pt-6 text-center border-t border-white/5 space-y-4">
                    <button
                        onClick={() => navigate('/')}
                        className="text-[10px] font-black uppercase tracking-[0.3em] text-slate-500 hover:text-brand-400 transition-colors"
                    >
                        Bypass to Guest Mode ‚Üí
                    </button>
                    <p className="text-[9px] text-slate-600 font-mono uppercase tracking-widest leading-loose">
                        Local memory only. Global synchronization unavailable in guest mode.
                    </p>
                </div>
            </div>
        </motion.div>
    );
}
